package DesignPattern.MVC;

public class employeeClass {
    private String name;
    private String address;
    private Integer age;
    private Integer telNo;
    private String jobRoll;
    private Integer empNo;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public Integer getTelNo() {
        return telNo;
    }

    public void setTelNo(Integer telNo) {
        this.telNo = telNo;
    }

    public String getJobRoll() {
        return jobRoll;
    }

    public void setJobRoll(String jobRoll) {
        this.jobRoll = jobRoll;
    }

    public Integer getEmpNo() {
        return empNo;
    }

    public void setEmpNo(Integer empNo) {
        this.empNo = empNo;
    }
}
